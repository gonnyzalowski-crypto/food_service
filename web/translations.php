/**
 * Get translation for a key
 */
function __($key, $lang = null) {
    global $translations;
    if ($lang === null) {
        $lang = $_SESSION['lang'] ?? 'de';
    }
    return $translations[$key][$lang] ?? $translations[$key]['en'] ?? $key;
}

/**
 * Echo translation
 */
function _e($key, $lang = null) {
    echo __($key, $lang);
}
